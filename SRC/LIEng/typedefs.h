#pragma once

////////////////////////////////////////////////////////////////////////////////
namespace interpreter {

////////////////////////////////////////////////////////////////////////////////
//						 Основные типы данных
////////////////////////////////////////////////////////////////////////////////
	typedef unsigned long  int dword;
	typedef unsigned short int word;
	typedef unsigned char byte;

	typedef signed long  int sdword;
	typedef signed short int sword;
	typedef signed char sbyte;

////////////////////////////////////////////////////////////////////////////////
//					Способ хранения опкода команды
////////////////////////////////////////////////////////////////////////////////

	typedef dword offset;
	typedef word  opcode;

////////////////////////////////////////////////////////////////////////////////
//								Регистры
////////////////////////////////////////////////////////////////////////////////

	// 8-битный регистр
	typedef byte reg8;

	// 16-битный регистр
	union reg16 {
		struct {
			reg8 lb, hb;
		};
		word w;

		inline operator word () {
			return w;
		}

		inline reg16& operator = ( word wd ) {
			w = wd; return *this;
		}
	};

	// 32-битный регистр
	union reg32 {
		struct {
			reg16 lw, hw;
		};
		dword dw;
		
		inline operator dword () {
			return dw;
		}
		
		inline reg32& operator = ( dword dd ) {
			dw = dd; return *this;
		}
	};

////////////////////////////////////////////////////////////////////////////////
}	// namespace interpreter

